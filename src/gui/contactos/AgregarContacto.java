    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        //verificamos que todos los datos hayan sido capturados en el formulario
        if(jTextFieldNombres.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El nombre del contacto es obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldNombres.requestFocus();
        }else if (jTextFieldApellidoPaterno.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El apellido paterno del contacto es obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldApellidoPaterno.requestFocus();
    }//GEN-LAST:event_jButtonAceptarActionPerformed
     else if (jTextFieldApellidoMaterno.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El apellido materno del contacto es obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldApellidoMaterno.requestFocus(); 
    }else if (jTextFieldCalle.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El calle es un dato obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldCalle.requestFocus();
    }else if (jTextFieldNumero.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El numero de casa es un dato obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldNumero.requestFocus();
    
    }else if (jTextFieldColonia.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "El colonia es un dato obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jTextFieldColonia.requestFocus();
    }else if (jDateChooserFechaNacimiento.getDate().equals("")){
            JOptionPane.showConfirmDialog(null, "La fecha de nacimiento es un dato obligatorio",
                    "AgendITA: Agregar contacto", JOptionPane.ERROR_MESSAGE);
                    jDateChooserFechaNacimiento.requestFocusInWindow();
    }else{
        //Agregamos los datos a la tabla contactos
        String nombres = jTextFieldNombres.getText();
        String ape_paterno = jTextFieldApellidoPaterno.getText();
        String ape_materno = jTextFieldApellidoMaterno.getText();
        
        String fecha_nac = "1999-12-31";
        
            if (jDateChooserFechaNacimiento.getDate() != null) {
                SimpleDateFormat dcn = new SimpleDateFormat ("yyyy-MM-dd");
                fecha_nac = dcn.format(jDateChooserFechaNacimiento.getDate());
            }
            
            int idGenero = buscarIdGenero(String.valueOf(jComboBoxGenero.getSelectedItem()));
            
            String calle = jTextFieldCalle.getText();
            int numero = 0;
            if(!jTextFieldNumero.getText().equals("")){
                numero = Integer.parseInt(jTextFieldNumero.getText());
            }
            
            String colonia = jTextFieldColonia.getText();
            
            Municipio municipio = (Municipio) jComboBoxMunicipio.getSelectedItem();
            int idMunicipio = municipio.getId();
            int idEstado = municipio.getIdEstado();
            
            int resultado1 = OperacionesBaseDatos.insertarContacto(nombres, ape_paterno, ape_materno,
                fecha_nac,idGenero, rutaFoto, calle, numero, colonia, idMunicipio, idEstado);
            int resultado2 = 0;
            int resultado3 = 0;
            
            //posteriormente agregamos los numeros telefonicos y los correos elecronicos
            
            int idContacto = OperacionesBaseDatos.ultimoRegistroInsertado();
        if(modeloListaTelefonos.size() > 0){
            
                //si hay al menos un telefono en la lista de telefonos
                for (int i = 0; i < modeloListaTelefonos.size(); i++) {
                    String telefono = String.valueOf(modeloListaTelefonos.getElementAt(i));
                    resultado2 = OperacionesBaseDatos.insertarTelefono(idContacto, telefono);     
            }
        }
            if (modeloListaCorreosElectronicos.size() > 0) {
                for (int i = 0; i < modeloListaCorreosElectronicos.size(); i++) {
                    String correo = String.valueOf(modeloListaTelefonos);
                    resultado3 = OperacionesBaseDatos.insertarCorreoElectronico(idContacto, correo);
                    
                }
            }
            
            if(resultado1 != -1 && resultado2 != -1 && resultado3 != -1)
            JOptionPane.showMessageDialog(null, "Registro insertado con exito", 
                    "AgendITA: Agregar contacto", JOptionPane.INFORMATION_MESSAGE);
    }
        
        //salimos del formulario de altas de contactos
        limpiarFormulario();
        this.setVisible(false);
        GestionContactos gestionContactos = GestionContactos.getInstancia();
        gestionContactos.setVisible(true);
   
    }
                        
