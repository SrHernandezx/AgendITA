    private void jButtonAgregarFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarFotoActionPerformed
        
        JFileChooser fc = new JFileChooser();
        File workingDirectory = new File(System.getProperty("user.dir"));
        fc.setCurrentDirectory(workingDirectory);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagenes", "PNG","JPG");
        fc.setFileFilter(filtro);
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int respuesta = fc.showOpenDialog(this);
        
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            try{
                File archivo = fc.getSelectedFile();
                copiarFoto(archivo);
                BufferedImage img = ImageIO.read(archivo);
                int alto = img.getHeight();
                int ancho = img.getWidth();
                int nuevoAlto = 0;
                int nuevoAncho = 0;
                if (alto > ancho) {
                    nuevoAlto = 235;
                    nuevoAncho = ancho * 235 / alto;
                }else if (ancho > alto) {
                    nuevoAlto = alto * 235 / ancho;
                    nuevoAncho = 235;
                }else{
                    nuevoAlto = 235;
                    nuevoAncho = 235;
                }
                Image nuevaImg = img.getScaledInstance(nuevoAncho, nuevoAlto,
                        java.awt.Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(nuevaImg);
                mostrarImagen(imageIcon);
            } catch (IOException ex) {
                Logger.getLogger(AgregarContacto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    
    }//GEN-LAST:event_jButtonAgregarFotoActionPerformed

